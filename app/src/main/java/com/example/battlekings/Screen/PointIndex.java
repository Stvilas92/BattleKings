package com.example.battlekings.Screen;

public class PointIndex {
    int indexX,indexY;

    public PointIndex(int indexX, int indexY) {
        this.indexX = indexX;
        this.indexY = indexY;
    }

    /**
     * Get the index X of the point
     * @return
     */
    public int getIndexX() {
        return indexX;
    }

    /**
     * Get the index Y of the point
     * @return
     */
    public int getIndexY() {
        return indexY;
    }

}
